# app/utils/file_utils.py
# ----------------------------------------------------------------------------------
# 文件工具模块 (File Utils)
# 作用：提供通用的文件 I/O 操作，如图像加载等。
# ----------------------------------------------------------------------------------

import os
from PIL import Image
import numpy as np

# ----------------------------------------------------------------------------------
# 函数：加载图像 (load_image)
# 作用：读取图像文件并转换为 NumPy 数组，便于后续处理。
# 参数：file_path - 图像文件路径
# 返回：numpy.ndarray
# ----------------------------------------------------------------------------------
def load_image(file_path: str) -> np.ndarray:
    img = Image.open(file_path)
    return np.array(img)
