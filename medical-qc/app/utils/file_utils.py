import os
from PIL import Image
import numpy as np

# ----------------------------------------------------------------------------------
# 函数：加载图像 (load_image)
# 作用：读取图像文件并转换为 NumPy 数组，便于后续处理。
# 参数：file_path - 图像文件路径
# 返回：numpy.ndarray (H, W, C)
# ----------------------------------------------------------------------------------
def load_image(file_path: str) -> np.ndarray:
    """
    通用图像加载函数
    """
    img = Image.open(file_path)
    return np.array(img)
